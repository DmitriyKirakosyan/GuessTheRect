    class func getRandomSet() -> [UIColor] {
        let sets = [set1, set2, set3, set4, set5, set6]
        return sets[random() % sets.count]
    }
    
    class func getRandomColors(set: [UIColor], num: Int) -> [UIColor] {
        var colors = shuffle(set)
        while (num > colors.count) {
            colors += shuffle(set)
        }
        return Array(colors[0..<num])
    }
    
    class func shuffle(setOfColors: [UIColor]) -> [UIColor] {
        return sorted(setOfColors) {(_, _) in arc4random() % 2 == 0}
    }
    
    class func getColor(#r: Int, g: Int, b: Int) -> UIColor {
        return UIColor(red: CGFloat(r)/255, green: CGFloat(g)/255, blue: CGFloat(b)/255, alpha: 1.0)
    }
